    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
        
        if(tableau.getSelectedRow()>=0){
            //Recuperons les id
            String[] value=tableau.getValueAt(tableau.getSelectedRow(), 1).toString().split("/");
            String idh=value[0];
            String idedt=value[1];
            String idlit=value[2];
            HospitaliserPK pk=new HospitaliserPK();
            pk.setIdetudiant(new BigInteger(idedt));
            pk.setIdhosp(new BigInteger(idh));
            pk.setIdlit(new BigInteger(idlit));
            Hospitaliser h=new Hospitaliser();
            h.setHospitaliserPK(pk);
            
            DialogAddOrdonnance ad=new DialogAddOrdonnance(this,true);
            ad.setHospi(h);
            ad.setLocationRelativeTo(null);
            ad.setVisible(true);
        }
    }//GEN-LAST:event_jButton6ActionPerformed

