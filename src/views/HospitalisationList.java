    public void remplirTableau(List<Hospitaliser> lhp){
        
        //Obtenir le nombre d'elements presents dans le tableau
        int nombrepresent = model.getRowCount();
        //Verifier quil ny a rien dedans
        int i = nombrepresent;
        if (nombrepresent > 0){
            i--;
            while (i >= 0) {
                //Retirer les elemets du tableau
                model.removeRow(i);
                i--;
            }
        }
        
         //Le remplir par la liste
        if (lhp != null) {
            if (lhp.size() > 0) {
                for (int j = 0; j < lhp.size(); j++) {
                    Hospitaliser h = lhp.get(j);

                    model.addRow(new String[]{(j+1)+"",h.getHospitaliserPK().getIdhosp()+"/"+h.getEtudiant().getIdetudiant()+"/"+h.getLit().getIdlit(),h.getEtudiant().getNom()+" "+h.getEtudiant().getPrenom(), h.getLit().getCode(), h.getLit().getIdsalle().getLibelle()});
                    
                    //,h.getHospitaliserPK().getIdhosp()+" / "+h.getHospitaliserPK().getIdetudiant()+" / "+h.getHospitaliserPK().getIdlit(),h.getEtudiant().getNom()+" "+h.getEtudiant().getPrenom(), h.getLit().getCode(), h.getLit().getIdsalle().getLibelle()
                }
            }
        }
    }
