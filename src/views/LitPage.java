     public void listLit(List<Lit> lits){
       //Obtenir le nombre d'elements presents dans le tableau
        int nombrepresent = model.getRowCount();
        //Verifier quil ny a rien dedans
        int i = nombrepresent;
        if (nombrepresent > 0){
            i--;
            while (i >= 0) {
                //Retirer les elemets du tableau
                model.removeRow(i);
                i--;
            }
        }
        
         //Le remplir par la liste
        if (lits != null) {
            if (lits.size() > 0) {
                for (int j = 0; j < lits.size(); j++) {
                    Lit l = lits.get(j);

                    model.addRow(new String[]{(j+1)+"", l.getIdlit()+"", l.getCode(), l.getIdsalle().getLibelle()});
                }
            }
        }
        
     }
    
