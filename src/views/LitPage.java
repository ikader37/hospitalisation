    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        try {
            // TODO add your handling code here:
            String report="src/reports/litReport.jrxml";
            JasperReport jr=JasperCompileManager.compileReport(report);
            
            JasperPrint jp=JasperFillManager.fillReport(jr, null,SingletonConnection.getConnecter());
            File pdf = File.createTempFile("output.", ".pdf");
            JasperExportManager.exportReportToPdfFile(jp,"lit.pdf");//Le fichier reproduit sera stocker dans le repertoire C
            JasperViewer.viewReport(jp,false);
            JOptionPane.showMessageDialog(null, "Vous pouvez voir le fichier PDF Dans la racine du projet sous le nom de lit.pdf");
        } catch (JRException ex) {
            Logger.getLogger(LitPage.class.getName()).log(Level.SEVERE, null, ex);
             JOptionPane.showMessageDialog(null, "Erreur.Veuillez reessayer svp");
        } catch (IOException ex) {
            Logger.getLogger(LitPage.class.getName()).log(Level.SEVERE, null, ex);
             JOptionPane.showMessageDialog(null, "Erreur.Veuillez reessayer svp");
        }
        
        
    }//GEN-LAST:event_jButton5ActionPerformed

