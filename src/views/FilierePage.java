    public void listFiliere(List<Filiere> filieres) {
        //Obtenir le nombre d'elements presents dans le tableau
        int nombrepresent = model.getRowCount();
        //Verifier quil ny a rien dedans
        int i = nombrepresent;
        if (nombrepresent > 0) {
            i--;
            while (i >= 0) {
                //Retirer les elemets du tableau
                model.removeRow(i);
                i--;
            }
        }

        //Le remplir par la liste
        if (filieres != null) {
            if (filieres.size() > 0) {
                for (int j = 0; j < filieres.size(); j++) {
                    Filiere l = filieres.get(j);

                    model.addRow(new String[]{(j + 1) + "", l.getIdfiliere() + "", l.getLibelle()});
                }
            }
        }

    }

